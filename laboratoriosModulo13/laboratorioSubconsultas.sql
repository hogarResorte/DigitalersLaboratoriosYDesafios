SELECT * FROM PRODUCTOS_NEPTUNO WHERE PRECIOUNIDAD > (SELECT MAX(PRECIOUNIDAD) FROM PRODUCTOS_SUSPENDIDOS) ORDER BY PRECIOUNIDAD DESC;
#3
