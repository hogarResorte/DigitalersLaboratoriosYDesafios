#4
SELECT * FROM PRODUCTOS_NEPTUNO WHERE LEFT(NOMBREPRODUCTO, 1) = (SELECT LEFT(NOMBRE_EMPLEADO, 1) FROM EMPLEADOS WHERE IDEMPLEADO = 8) ORDER BY NOMBREPRODUCTO;
